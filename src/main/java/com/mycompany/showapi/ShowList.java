    public void showSearch(String search, int page) {
        String url = "https://www.episodate.com/api/search?q=" + search + "&page=" + page;
        try {
            synchronized(myLock){
                currentResponse = SearchResults.fill(url);
                updateSearchResults();
            }
        } catch (Exception ex) { 
        }
    }
 
    public void threadedSearch(String userSearch, int pageNumber) {
        Thread thread = new Thread(new Runnable(){
            public void run() {
                showSearch(userSearch, pageNumber);
            }
        }); 
        thread.start();   
    }
    
