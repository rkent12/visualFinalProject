    public void updateSearchResults() {
        //System.out.println(currentResponse);
        ArrayList<ShowResults> tempShows = currentResponse.getTV_shows();
        dlmResults.clear();
        for(int i = 0; i < tempShows.size(); i++) {
            dlmResults.addElement(tempShows.get(i));
        }
       currentPage.setText(currentResponse.getPage() + "/" + currentResponse.getPages());
       
    }
    public void updateCurrentShow(){
        startDate.setText(currentShow.getStart_date());
        showStatus.setText(currentShow.getStatus());
        showCountry.setText(currentShow.getCountry());
        showNetwork.setText(currentShow.getNetwork());
    }
    
    public void saveList() throws Exception {
        Gson gson = new Gson();
        File file = new File("/Users/ryankent/Desktop/VP1/test.json");
        FileWriter writer = new FileWriter(file);
        String json = "{";
        for(int i = 0; i < tempList.size(); i++){
            String data = gson.toJson(tempList.get(i), ShowResults.class);
            json += data + ",";
        }
        json += "}";
   
        writer.write(json);
        writer.close();
    }
    
    
    

